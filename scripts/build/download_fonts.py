#!/usr/bin/env python3
"""Download self-hosted WOFF2 fonts (Inter, IBM Plex Mono, Allerta Stencil)."""

from __future__ import annotations

import re
import urllib.request
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent.parent
OUT = ROOT / "src" / "coh_ucs_tools" / "web" / "static" / "fonts"
CSS_OUT = ROOT / "src" / "coh_ucs_tools" / "web" / "static" / "css" / "fonts.css"

# Latin + latin-ext covers English UI and most European CoH locale names.
_SUBSETS = frozenset({"latin", "latin-ext"})

_UA = (
    "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 "
    "(KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36"
)

_SPECS = (
    ("inter", "Inter:wght@400;500", "inter"),
    ("mono", "IBM+Plex+Mono:wght@400;500", "ibm-plex-mono"),
    ("stencil", "Allerta+Stencil", "allerta-stencil"),
)


def _fetch_css(family: str) -> str:
    url = f"https://fonts.googleapis.com/css2?family={family}&display=swap"
    req = urllib.request.Request(url, headers={"User-Agent": _UA})
    with urllib.request.urlopen(req, timeout=30) as resp:
        return resp.read().decode("utf-8")


def _parse_faces(css: str) -> list[tuple[str, str, str, str]]:
    """Return (subset, weight, url, unicode_range) for each @font-face block."""
    faces: list[tuple[str, str, str, str]] = []
    parts = re.split(r"/\* ([^*]+) \*/", css)
    subset = "latin"
    for i, chunk in enumerate(parts):
        if i % 2 == 1:
            subset = chunk.strip()
            continue
        for block in re.findall(r"@font-face\s*\{[^}]+\}", chunk):
            weight = re.search(r"font-weight:\s*(\d+)", block)
            url = re.search(r"url\((https://fonts\.gstatic\.com/[^)]+\.woff2)\)", block)
            urange = re.search(r"unicode-range:\s*([^;]+);", block)
            if weight and url:
                faces.append((
                    subset,
                    weight.group(1),
                    url.group(1),
                    urange.group(1).strip() if urange else "",
                ))
    return faces


def _download_faces(faces: list[tuple[str, str, str, str]], prefix: str) -> list[tuple[str, str, str, str]]:
    """Download filtered faces; return (family_file, weight, path, unicode_range)."""
    saved: list[tuple[str, str, str, str]] = []
    for subset, weight, url, urange in faces:
        if subset not in _SUBSETS:
            continue
        fname = f"{prefix}-{weight}-{subset}.woff2"
        dest = OUT / fname
        req = urllib.request.Request(url, headers={"User-Agent": _UA})
        with urllib.request.urlopen(req, timeout=60) as resp:
            dest.write_bytes(resp.read())
        saved.append((fname, weight, urange, subset))
        print(f"  {fname} ({dest.stat().st_size // 1024} KiB)")
    return saved


def _write_fonts_css(families: dict[str, tuple[str, list[tuple[str, str, str, str]]]]) -> None:
    blocks = ["/* Self-hosted fonts — generated by scripts/download_fonts.py */"]
    for _key, (family, faces) in families.items():
        blocks.append(f"/* {family} */")
        for fname, weight, urange, _subset in faces:
            rule = [
                "@font-face {",
                f'  font-family: "{family}";',
                "  font-style: normal;",
                f"  font-weight: {weight};",
                "  font-display: swap;",
                f'  src: url("../fonts/{fname}") format("woff2");',
            ]
            if urange:
                rule.append(f"  unicode-range: {urange};")
            rule.append("}")
            blocks.append("\n".join(rule))
    CSS_OUT.write_text("\n\n".join(blocks) + "\n", encoding="utf-8")
    print(f"Wrote {CSS_OUT}")


def main() -> None:
    OUT.mkdir(parents=True, exist_ok=True)
    for old in OUT.glob("*.woff2"):
        old.unlink()

    families: dict[str, tuple[str, list[tuple[str, str, str, str]]]] = {}
    for key, spec, prefix in _SPECS:
        print(f"Fetching {spec}…")
        faces = _parse_faces(_fetch_css(spec))
        family = {
            "inter": "Inter",
            "mono": "IBM Plex Mono",
            "stencil": "Allerta Stencil",
        }[key]
        downloaded = _download_faces(faces, prefix)
        families[key] = (family, downloaded)

    _write_fonts_css(families)


if __name__ == "__main__":
    main()
